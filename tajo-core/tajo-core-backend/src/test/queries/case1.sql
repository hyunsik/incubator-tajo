SELECT
  SVC_MGMT_NUM,
  GROUP_CODE

FROM (
  SELECT *
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'A'

  UNION ALL

  SELECT *
  FROM PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'B'

  UNION ALL

  SELECT
    *
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'D'
  ORDER BY
    POINT DESC
  LIMIT 50000

  UNION ALL

  SELECT
    *
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'E'
  ORDER BY
    POINT DESC LIMIT 10000

  UNION ALL

  SELECT
    *
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'F'
  ORDER BY
    POINT DESC
  LIMIT 50000

  UNION ALL

  SELECT
    *
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'G'
  ORDER BY
    POINT DESC
  LIMIT 10000
) T

GROUP BY
  SVC_MGMT_NUM,
  GROUP_CODE

ORDER BY
  GROUP_CODE,
  SVC_MGMT_NUM