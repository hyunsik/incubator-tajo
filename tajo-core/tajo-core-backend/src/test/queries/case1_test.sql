SELECT
  SVC_MGMT_NUM,
  GROUP_CODE,
  POINT

FROM (
  SELECT
    SVC_MGMT_NUM, GROUP_CODE, POINT
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'A'

  UNION ALL

  SELECT
    SVC_MGMT_NUM, GROUP_CODE, POINT
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'B'

  UNION ALL

  SELECT
    SVC_MGMT_NUM, GROUP_CODE, POINT
  FROM
    PPAKKU_ONEMOUNT_3RD_FINAL_GROUP_tajo
  WHERE
    GROUP_CODE = 'D'
  ORDER BY
    POINT DESC
  LIMIT 50000
) T

-- GROUP BY
--   SVC_MGMT_NUM,
--   GROUP_CODE

ORDER BY
  GROUP_CODE,
  SVC_MGMT_NUM